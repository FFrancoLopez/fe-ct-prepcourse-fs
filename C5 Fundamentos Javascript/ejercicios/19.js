function suma(x, y) {
  // La función recibe dos argumentos llamados "x" e "y" que son números.
  // Retorna el resultado de la suma de estos argumentos.
  // Por ejemplo: 
  // 5, 5 ---> 10
  // -5, 5 ---> 0
  // Tu código:
  // Indicamos que nos retorne el resultado numerico de nuestra suma. 
  return (x + y); 
}

module.exports = suma; 